package dio.metodos;

public class calculadora1 {
    public static void soma(double a, double b){
        double resultado = a + b;
        System.out.println("O valor " + a + " somado por " + b + " e " + resultado);
    }
    public static void subtracao(double a, double b){
        double resultado = a - b;
        System.out.println("O valor " + a + " subtraido por " + b + " e " + resultado);
    }
    public static void multiplicacao(double a, double b){
        double resultado = a * b;
        System.out.println("O valor " + a + " multiplicado por " + b + " e " + resultado);
    }
    public static void divisao(double a, double b){
        double resultado = a / b;
        System.out.println("O valor " + a + " dividido por " + b + " e " + resultado);
    }
}


